package tipoDeDatoVariables;

/*realize un algoritmo con dos variables de números enteros y luego muestre la suma de esos dos números.*/

public class Ejercicio1{
	public static void main(String[]args) {
		//Inicializamos dos variables con numeros enteros positivos para realizar la suma
		int valorA=20;
		int valorB=10;
		//Ya despues de crear las variables creamos una tercera variable llamado suma que contenta las dos variables anteriores
		int suma = valorA + valorB;
		//Las variables siempre deben cerrarse con ';' para darle finalizacion a la linea de codigo
		//ya habiendo creado la variable suma, ahora vamos a imprimir en consola el resultado de esta operacion
		System.out.println(suma);
		
	}
	

}
