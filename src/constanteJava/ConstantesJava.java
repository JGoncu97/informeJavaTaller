package constanteJava;

public class ConstantesJava {
	public static void main(String[]args) {
		final int NUMERO= 1234;
		System.out.println("El valor de la constante de tipo Int es:" +NUMERO);
		
		final double PORCIENTO= 0.30;
		System.out.println("El valor de la constante de tipo Double es:" +PORCIENTO);
		
		final String PALABRA = "Hola";
		System.out.println("El valor de la constante de tipo String es:" +PALABRA);
		
		
		
	}
}
